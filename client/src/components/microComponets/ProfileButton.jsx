import { Menu, Transition } from '@headlessui/react'
import { Fragment} from 'react'
import { ChevronUpIcon } from '@heroicons/react/20/solid'
import { useNavigate } from 'react-router-dom';
import { toast } from 'react-toastify';
import { UseAuth } from '../../context/Auth';

export default function ProfileButton(props) {
  const [auth] = UseAuth();
    const name = props.name
    const id = props.id
    const email = props.email
    const navigate = useNavigate();
    

    const handleViewProfile = ()=>{
      const dataToSend = id;
      props.openProfile(dataToSend);

    };

    const handleLogout = ()=>{
      localStorage.setItem('whatsapp','');
      toast.success("Logout successfully")
      navigate('/')
    }

  return (
    <div className="top-16 w-62 text-right">
      <Menu as="div" className="relative inline-block text-left">
        <div>
          <Menu.Button className="h-[9vh] px-3 inline-flex w-full justify-center rounded-md bg-black bg-opacity-20 py-1 text-sm font-medium text-white hover:bg-blue-300 hover:text-black focus:outline-none focus-visible:ring-2 focus-visible:ring-white focus-visible:ring-opacity-75">
          <div className="flex flex-row">
                    <div ><img className="w-10 h-10 rounded-full" src={`https://echochatserver.vercel.app/api/auth/profile-photo/${auth?.user?._id}`} alt="profile" /></div>
                    <div className='ms-3'>Welcome: {name} <br /> ID: {email}</div>
                </div>
            <ChevronUpIcon
              className="ml-2 -mr-1 h-5 w-5 text-violet-200 hover:text-violet-100"
              aria-hidden="true"
            />
          </Menu.Button>
        </div>
        <Transition
          as={Fragment}
          enter="transition ease-out duration-100"
          enterFrom="transform opacity-0 scale-95"
          enterTo="transform opacity-100 scale-100"
          leave="transition ease-in duration-75"
          leaveFrom="transform opacity-100 scale-100"
          leaveTo="transform opacity-0 scale-95"
        >
          <Menu.Items className="absolute -translate-y-[6rem] translate-x-10 top-0 mt-2 w-56 origin-top-right divide-y divide-gray-100 rounded-md bg-white shadow-lg ring-1 ring-black ring-opacity-5 focus:outline-none z-40">
            <div className="px-1 py-1 ">
              <Menu.Item>
                {({ active }) => (
                  <button
                    onClick={handleViewProfile}
                    className={`${
                      active ? 'bg-violet-500 text-white' : 'text-gray-900'
                    } group flex w-full items-center rounded-md px-2 py-2 text-sm`}
                  >
                    {active ? (
                      <EditActiveIcon
                        className="mr-2 h-5 w-5"
                        aria-hidden="true"
                      />
                    ) : (
                      <EditInactiveIcon
                        className="mr-2 h-5 w-5"
                        aria-hidden="true"
                      />
                    )}
                   View Profile
                  </button>
                )}
              </Menu.Item>
            </div>
            <div className="px-1 py-1">
              <Menu.Item>
                {({ active }) => (
                  <button
                  onClick={handleLogout}
                    className={`${
                      active ? 'bg-violet-500 text-white' : 'text-gray-900'
                    } group flex w-full items-center rounded-md px-2 py-2 text-sm`}
                  >
                    {active ? (
                      <DeleteActiveIcon
                        className="mr-2 h-5 w-5 text-violet-400"
                        aria-hidden="true"
                      />
                    ) : (
                      <DeleteInactiveIcon
                        className="mr-2 h-5 w-5 text-violet-400"
                        aria-hidden="true"
                      />
                    )}
                    Log Out
                  </button>
                )}
              </Menu.Item>
            </div>
          </Menu.Items>
        </Transition>
      </Menu>
    </div>
  )
}

